
function parser(reader) {
    var result = {};
    while (reader.remain()) {
        var info = [
            reader.readUINT32()
                    , reader.readString16()
                    , reader.readUByte()
        ];
        var arr = info[1].split("=");
        var name = arr[1];
        var num = Number(arr[2]);
        info[3] = num;
        result[name] = info;
        //API.info(API.encodeJson(info));
    }
    return result;
}

